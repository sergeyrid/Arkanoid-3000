import Brick from "./brick.js";

export let newLevel = function(game, level) {
  for (let i = 0; i < 7; i++) {
    for (let j = 0; j < 7; j++) {
      game.bricks.pop();
    }
  }
  for (let i = 0; i < 7; i++) {
    for (let j = 0; j < 7; j++) {
      game.bricks.push(
        new Brick(game, { x: i * game.gameWidth / 7.27 + 15, y: j * game.gameHeight / 10 + 10 }, level[j][i])
      );
    }
  }
};

